# 5. Напишите программу, которая принимает на вход координаты двух точек
#   и находит расстояние между ними в 2D пространстве.
#   https://ru.onlinemschool.com/math/library/analytic_geometry/point_point_length/
#   in
#   - 3
#   - 6
#   - 2
#   - 1
#   out
#   5.099
# =======================================================================

print("Задача 5:")
coord_x_a = int(input("Введите координату Х точки A: "))
coord_y_a = int(input("Введите координату Y точки A: "))
coord_x_b = int(input("Введите координату Х точки B: "))
coord_y_b = int(input("Введите координату Y точки B: "))
ab = ((coord_x_b - coord_x_a) ** 2 + (coord_y_b - coord_y_a) ** 2) ** 0.5
print("Расстояние между точками А и В в 2D пространстве: ", f"{ab:.3f}")
