# 3. Напишите программу, которая принимает на вход координаты точки (X и Y),
#    причём X ≠ 0 и Y ≠ 0 и выдаёт номер четверти плоскости, в которой
#    находится эта точка (или на какой оси она находится).
#  *Пример:*
#   - x=34; y=-30 -> 4
#   - x=2; y=4-> 1
#   - x=-34; y=-30 -> 3
# =====================================================================
print("Задача 3:")
coord_x = int(input("введите координату X для точки: "))
coord_y = int(input("введите координату Y для точки: "))
if coord_x == 0 or coord_y == 0:
    print("задайте координаты отличные от нуля!")
else:
    if coord_x > 0 and coord_y > 0:
        print("1")
    elif coord_x < 0 and coord_y > 0:
        print("2")
    elif coord_x < 0 and coord_y < 0:
        print("3")
    elif coord_x > 0 and coord_y < 0:
        print("4")

