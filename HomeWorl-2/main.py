# 1. Напишите программу, которая принимает на вход вещественное число
#    и показывает сумму его цифр.
# in -> out
# - 6782 -> 23
# - 0.67 -> 13
# - 198.45 -> 27
# =======================================================================
import random

print("Эадача 1:")
f_num = float(input("Введите вещественное число: ").replace(",", ".").replace("-", ""))
e = -1
stat_con = 1
while stat_con:
    e += 1
    a = f_num * 10 ** e
    stat_con = int(a) - a
num = int(a)
print("целое=", num)
summa = 0
while num:
    summa += num % 10
    num //= 10
print("Сумма цифр числа=", summa)

