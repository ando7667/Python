# 3. Задайте список из n чисел, заполненный по формуле (1 + 1/n) ** n и
#    выведите на экран их сумму.
#    Для n = 6: [2, 2, 2, 2, 2, 3] -> 13
# =======================================================================

print("Эадача 3:")
num = int(input("Введите целое число: ").replace("-", "").replace(",", ".").split(".")[0])
resultat = []
summa = 0
num += 1
for i in range(1, num):
    num_out = round((1 + 1 / i) ** i)
    resultat.append(num_out)
    summa += num_out
print("Список чисел = ", resultat)
print("Сумма чисел в списке = ", summa)

